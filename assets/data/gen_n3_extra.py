def parse():
    entries = []
    seen = set()
    for line in LINES.strip().split("\n"):
        parts = line.split("|")
        if parts[0].startswith("sincerely"):
            continue
        if len(parts) < 13:
            continue
        char = parts[0]
        if char in seen:
            continue
        seen.add(char)
        on, kun, eng, kor = parts[1], parts[2], parts[3], parts[4]
        strokes, grade = int(parts[5]), int(parts[6])
        w1, r1, m1 = parts[7], parts[8], parts[9]
        w2, r2, m2 = parts[10], parts[11], parts[12]
        entries.append({
            "character": char,
            "onyomi": on,
            "kunyomi": kun,
            "meanings": [eng, kor],
            "strokeCount": strokes,
            "grade": grade,
            "examples": [
                {"word": w1, "reading": r1, "meaning": m1},
                {"word": w2, "reading": r2, "meaning": m2}
            ]
        })
    return entries

if __name__ == "__main__":
    sys.stdout.reconfigure(encoding='utf-8')
    new_data = parse()
    script_dir = os.path.dirname(os.path.abspath(__file__))

    # Load existing N3
    n3_path = os.path.join(script_dir, "kanji_n3.json")
    existing = json.load(open(n3_path, encoding="utf-8"))
    existing_chars = set(e['character'] for e in existing)

    # Load other levels to check overlap
    skip = set(existing_chars)
    for level in ["n1", "n2", "n4", "n5"]:
        d = json.load(open(os.path.join(script_dir, f"kanji_{level}.json"), encoding="utf-8"))
        skip |= set(e['character'] for e in d)

    # Filter new entries
    added = []
    skipped = []
    for e in new_data:
        if e['character'] in skip:
            skipped.append(e['character'])
        else:
            added.append(e)
            skip.add(e['character'])

    if skipped:
        print(f"Skipped (overlap): {''.join(skipped)} ({len(skipped)})")

    # Merge with existing
    merged = existing + added
    with open(n3_path, "w", encoding="utf-8") as f:
        json.dump(merged, f, ensure_ascii=False, indent=2)
    print(f"N3: {len(existing)} -> {len(merged)} (+{len(added)} new)")
