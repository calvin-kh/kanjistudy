def parse():
    entries = []
    for line in LINES.strip().split("\n"):
        parts = line.split("|")
        if parts[0].startswith("sincerely"):
            continue
        char,on,kun,eng,kor = parts[0],parts[1],parts[2],parts[3],parts[4]
        strokes,grade = int(parts[5]),int(parts[6])
        w1,r1,m1 = parts[7],parts[8],parts[9]
        w2,r2,m2 = parts[10],parts[11],parts[12]
        entries.append({
            "character": char,
            "onyomi": on,
            "kunyomi": kun,
            "meanings": [eng, kor],
            "strokeCount": strokes,
            "grade": grade,
            "examples": [
                {"word": w1, "reading": r1, "meaning": m1},
                {"word": w2, "reading": r2, "meaning": m2}
            ]
        })
    return entries

if __name__ == "__main__":
    import sys
    sys.stdout.reconfigure(encoding='utf-8')
    data = parse()
    script_dir = os.path.dirname(os.path.abspath(__file__))

    # Load other levels to check overlap
    n3 = json.load(open(os.path.join(script_dir, "kanji_n3.json"), encoding="utf-8"))
    n4 = json.load(open(os.path.join(script_dir, "kanji_n4.json"), encoding="utf-8"))
    n5 = json.load(open(os.path.join(script_dir, "kanji_n5.json"), encoding="utf-8"))
    skip = set(e['character'] for e in n3) | set(e['character'] for e in n4) | set(e['character'] for e in n5)

    # Filter out overlaps
    unique = [e for e in data if e['character'] not in skip]
    removed = [e['character'] for e in data if e['character'] in skip]
    if removed:
        print(f"Removed overlaps: {''.join(removed)}")

    out = os.path.join(script_dir, "kanji_n2.json")
    with open(out, "w", encoding="utf-8") as f:
        json.dump(unique, f, ensure_ascii=False, indent=2)
    print(f"Generated {len(unique)} N2 entries (removed {len(removed)} overlaps)")
